  void
  grWriteCellChar( grBitmap*  target,
                   int        x,
                   int        y,
                   int        charcode,
                   grColor    color )
  {
    if ( charcode < 0   ||
         charcode > 255 )
      return;

    gr_charcell.buffer = font[charcode];
    grBlitGlyphToSurface( (grSurface*)target, &gr_charcell, x, y, color );
  }


  void
  grWriteCellString( grBitmap*    target,
                     int          x,
                     int          y,
                     const char*  string,
                     grColor      color )
  {
    while ( *string )
    {
      gr_charcell.buffer = font[*(unsigned char*)string++];
      grBlitGlyphToSurface( (grSurface*)target, &gr_charcell, x, y, color );
      x += 8;
    }
  }


